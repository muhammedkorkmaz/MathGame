package com.softonetech.mathgame;

public class Choices {

    private int A;
    private int B;
    private int C;
    private boolean IsATrue;
    private boolean IsBTrue;
    private boolean IsCTrue;

    public int getA() {
        return A;
    }

    public void setA(int a) {
        A = a;
    }

    public int getB() {
        return B;
    }

    public void setB(int b) {
        B = b;
    }

    public int getC() {
        return C;
    }

    public void setC(int c) {
        C = c;
    }

    public boolean isATrue() {
        return IsATrue;
    }

    public void setATrue() {
        IsATrue = true;
    }

    public boolean isBTrue() {
        return IsBTrue;
    }

    public void setBTrue() {
        IsBTrue = true;
    }

    public boolean isCTrue() {
        return IsCTrue;
    }

    public void setCTrue() {
        IsCTrue = true;
    }
}
